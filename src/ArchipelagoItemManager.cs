        public ArchipelagoItemManager() {
            nexmexFound = GetSavedNexMexCount();
        }

        private int GetSavedNexMexCount()
        {
            if (MPlayer.instance.data.TryGetValue(Constants.NexmexCountFlag, out string value))
            {
                if (Int32.TryParse(value, out int val)) return val;
                else
                {
                    WolapPlugin.Log.LogError($"Could not parse the {Constants.NexmexCountFlag} flag count to an int.");
                    return 0;
                }
            }
            else return 0;
        }

        public void ResetItemCounts()
        {
            nexmexFound = GetSavedNexMexCount();
        }

        public bool ProcessItem(string itemName)
        {
            if (itemName.IsNullOrWhiteSpace()) return false;

            ArchipelagoItem item = itemList.Find(item => item.Name == itemName);
            if (item == null) return false;

            //TODO: Items that require special attributes/enchantments or other info (e.g. ghostwood visitor's permit/ID)
            switch (itemName)
            {
                case "Progressive Nex-Mex Skillbook":
                    //Might have an AP setting to randomize order later
                    bool result = GiveItem(nexmexBooks[nexmexFound]);
                    nexmexFound++;
                    MPlayer.instance.data[Constants.NexmexCountFlag] = nexmexFound.ToString();
                    return result;
                case "Ghost Coach To Gun Manor":
                    WolapPlugin.UIManager.RcvItemQueue.Enqueue(new UIManager.ReceivedID(item.IDs[0], 1));
                    WolapPlugin.Log.LogInfo("Enabling Gun Manor coach in Dirtwater.");
                    //Don't actually need to do anything here, can just use the generic received_item_{} flag as a condition for the coach to appear
                    return true;
                case "Pickaxe":
                case "Shovel":
                    Store.AddStockItem("dirtwatergeneral", ModelManager.GetItem(item.IDs[0]), -1, 1000);
                    goto default;
                default:
                    return GiveItem(item);
            }
        }

        public bool GiveItem(ArchipelagoItem item)
        {
            if (item == null) return false;

            for (int i = 0; i < item.IDs.Length; i++)
            {
                string id = item.IDs[i];
                int quantity = item.Quantities[i];

                WolapPlugin.Log.LogInfo("Giving the player " + id + " x " + quantity);

                string arg = id;
                if (quantity > 1) arg += "," + quantity;
                MCommand cmd = new MCommand(MCommand.Op.ADDITEM, [arg]);
                MScript script = new MScript("script_additem", new Dictionary<string, string>());
                MScript.State state = script.NewState("state_additem");
                state.AddCommand(cmd);
                state.Execute();

                WolapPlugin.UIManager.RcvItemQueue.Enqueue(new UIManager.ReceivedID(id, quantity));
            }

            return true;
        }

